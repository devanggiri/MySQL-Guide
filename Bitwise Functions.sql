-- BIT_AND: Returns the bitwise AND of all bits in a column
SELECT BIT_AND(column1) AS bitwise_and FROM table_name;

-- BIT_OR: Returns the bitwise OR of all bits in a column
SELECT BIT_OR(column1) AS bitwise_or FROM table_name;

-- BIT_XOR: Returns the bitwise XOR of all bits in a column
SELECT BIT_XOR(column1) AS bitwise_xor FROM table_name;
